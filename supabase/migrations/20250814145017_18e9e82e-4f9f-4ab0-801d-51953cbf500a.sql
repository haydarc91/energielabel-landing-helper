Wat is een energielabel?

Een energielabel toont hoe energiezuinig je woning is op een schaal van A (zeer zuinig) tot G (zeer onzuinig). Het label is gebaseerd op de energieprestatie van je woning en geeft potentiële kopers of huurders inzicht in de verwachte energiekosten.

Wanneer is een energielabel verplicht?

• Bij verkoop van een woning
• Bij verhuur van een woning  
• Bij oplevering van nieuwbouw
• Bij grote renovaties (vanaf €25.000)

Zonder geldig energielabel riskeer je een boete van maximaal €515 als particuliere eigenaar.

Energielabel aanvragen: actuele kosten 2025

De kosten voor een energielabel variëren per woningtype:

• Appartement/studio: €280 - €300
• Tussenwoning: €300 - €320  
• Hoekwoning: €320 - €340
• Vrijstaande woning: €340 - €380
• Vrijstaande woning >250m²: €380 - €420

Deze prijzen zijn inclusief BTW en gelden voor een definitief energielabel door een gecertificeerde EPA-adviseur.

Factoren die de prijs beïnvloeden:

• Grootte van de woning
• Complexiteit van de woning
• Bereikbaarheid van de woning
• Spoedservice (+€50-100)
• Regio (grote steden vaak duurder)

Hoe vraag je een energielabel aan?

Stap 1: Kies een gecertificeerde EPA-adviseur
Controleer altijd of de adviseur geregistreerd staat bij RVO (Rijksdienst voor Ondernemend Nederland).

Stap 2: Maak een afspraak
Plan een huisbezoek van ongeveer 1-2 uur, afhankelijk van de grootte van je woning.

Stap 3: Huisbezoek door de adviseur
De adviseur meet en fotografeert:
• Buitengevel en ramen
• Isolatie van muren, dak en vloer
• Verwarmingsinstallatie
• Ventilatie
• Eventuele zonnepanelen of andere duurzame installaties

Stap 4: Verwerking en oplevering
Binnen 5-10 werkdagen ontvang je het definitieve energielabel per e-mail.

