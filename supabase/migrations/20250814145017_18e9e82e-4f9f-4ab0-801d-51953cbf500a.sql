Waarom heeft elke woning een energielabel nodig?

Sinds 2008 is een energielabel verplicht bij verkoop, verhuur en oplevering van woningen in Nederland. Het label informeert potentiële kopers en huurders over de energieprestatie en verwachte energiekosten van de woning.

