Tips voor een beter energielabel

Voordat je het label aanvraagt:
• Laat gaten en kieren dichten
• Vervang oude cv-ketel
• Plaats HR++ glas
• Isoleer zolder/kruipruimte
• Overweeg zonnepanelen

Deze maatregelen kunnen je label van D naar B brengen, wat de woningwaarde met 4-6% kan verhogen.

Veelgemaakte fouten vermijden

• Wacht niet tot de laatste week voor verkoop
• Kies niet alleen op prijs, maar ook op service
• Zorg dat alle installaties toegankelijk zijn
• Bewaar alle documentatie van isolatie/installaties
• Plan op tijd bij spoedeisende verkoop

